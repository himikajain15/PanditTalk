class AppStrings {
  static String getString(BuildContext context, String key, {String? fallback}) {
    // Listen to language changes so widgets rebuild when language changes
    final langProvider = Provider.of<LanguageProvider>(context, listen: true);
    final locale = langProvider.locale.languageCode;
    final translations = _getTranslations(locale);
    return translations[key] as String? ?? fallback ?? key;
  }

  static String getStringWithParam(BuildContext context, String key, Map<String, String> params, {String? fallback}) {
    String text = getString(context, key, fallback: fallback);
    params.forEach((key, value) {
      text = text.replaceAll('{$key}', value);
    });
    return text;
  }

  static Map<String, String> _getTranslations(String locale) {
    switch (locale) {
      case 'hi':
        return _hindi;
      case 'mr':
        return _marathi;
      case 'gu':
        return _gujarati;
      case 'te':
        return _telugu;
      case 'kn':
        return _kannada;
      default:
        return _english;
    }
  }

